    private void BtMajPatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtMajPatActionPerformed
        if (txtIdPat.isEditable()) {
            txtIdPat.setEditable(false);
            txtNomPat.setEditable(true);
            txtPrePat.setEditable(true);
            txtTelPat.setEditable(true);
            JOptionPane.showMessageDialog(this, "Modifier les champs que vous souhaitez mettre à jour puis cliquer à nouveau sur Mise à jour", "Mise à jour", JOptionPane.INFORMATION_MESSAGE);
        } else {
            try {
                p.setNom(txtNomPat.getText());
                p.setPrenom(txtPrePat.getText());
                p.setTel(txtTelPat.getText());
                patdao.update(p);
                JOptionPane.showMessageDialog(this, "Patient mis à jour", "succès", JOptionPane.INFORMATION_MESSAGE);
                txtIdPat.setEditable(true);
                txtNomPat.setEditable(false);
                txtPrePat.setEditable(false);
                txtTelPat.setEditable(false);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage(), "ERREUR", JOptionPane.ERROR_MESSAGE);
            }
        }

    }//GEN-LAST:event_BtMajPatActionPerformed

    private void BtDelPatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtDelPatActionPerformed
        try {
            patdao.delete(p);
            JOptionPane.showMessageDialog(this, "Patient supprimer", "succès", JOptionPane.INFORMATION_MESSAGE);
            txtIdPat.setText(" ");
            txtNomPat.setText(" ");
            txtPrePat.setText(" ");
            txtTelPat.setText(" ");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "ERREUR", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_BtDelPatActionPerformed

