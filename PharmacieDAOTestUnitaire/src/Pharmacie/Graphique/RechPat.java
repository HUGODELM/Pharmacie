    private void btRechPatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btRechPatActionPerformed
        try {
            int id = Integer.parseInt(txtIdPat.getText());
            p = patdao.read(id);
            txtNomPat.setText(p.getNom());
            txtPrePat.setText(p.getPrenom());
            txtTelPat.setText(p.getTel());
            BtMajPat.setEnabled(true);
            BtDelPat.setEnabled(true);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "ERREUR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btRechPatActionPerformed

